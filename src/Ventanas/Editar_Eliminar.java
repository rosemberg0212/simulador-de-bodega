    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
       String item=CampoItem.getText();
       if(item.isEmpty())
       {
           JOptionPane.showMessageDialog(this, "Se requiere que ingrese el "
                   + "numero del item");
           return;
       }
       Validar Admi = new Validar();
        try {
            Inventario Objeto = Admi.buscarObjeto(item);
            CampoMaterial.setEditable(true);
            CampoMedidas.setEditable(true);
            CampoValorUnitario.setEditable(true);
            CampoCantidad.setEditable(true);
            CampoFecha.setEditable(true);
            BotonEditar.setEnabled(true);
            BotonEliminar.setEnabled(true);
            
            CampoMaterial.setText(Objeto.getDescripcion());
            CampoMedidas.setText(Objeto.getMedidas());
            CampoValorUnitario.setText(String.valueOf(Objeto.getValorUnitario()));
            CampoCantidad.setText(String.valueOf(Objeto.getCantidad()));
            CampoFecha.setText(Objeto.getFechaIngreso());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_AgregarActionPerformed

