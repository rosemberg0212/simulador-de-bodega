    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarActionPerformed
        int opcion = JOptionPane.showConfirmDialog(this, "Seguro que desea Eliminar los datos de los objetos", "Confimar Accion", JOptionPane.YES_NO_OPTION);
        if(opcion==JOptionPane.YES_OPTION)
        {
             String Item = CampoItem.getText();
            if(Item == null || Item.isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Es necesario ingresar el numero del item a eliminar");
                return;
            }
            else
            {
                if(Validar.listaDeObjetos.remove(Item) != null)
                {
                    JOptionPane.showMessageDialog(this, "Objeto eliminado del sistema");
                    CampoMaterial.setEditable(false);
                    CampoMedidas.setEditable(false);
                    CampoValorUnitario.setEditable(false);
                    CampoCantidad.setEditable(false);
                    CampoFecha.setEditable(false);
                 
                    CampoMaterial.setText("");
                    CampoMedidas.setText("");
                    CampoValorUnitario.setText("");
                    CampoCantidad.setText("");
                    CampoItem.setText("");
                    CampoFecha.setText("");
                    BotonEliminar.setEnabled(false);
                    BotonEditar.setEnabled(false);
                }
            }
        }
        Archivos ne = new Archivos();
        try {
            Archivos.guardarObjetos();
        } catch (IOException ex) {
            Logger.getLogger(Editar_Eliminar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_BotonEliminarActionPerformed

    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
         int opcion = JOptionPane.showConfirmDialog(this, "Seguro que desea editar los datos de los objetos", "Confimar Accion", JOptionPane.YES_NO_OPTION);
        if(opcion==JOptionPane.YES_OPTION)
        {
            String Item = CampoItem.getText();
       
            if(Item !=null && Item.isEmpty()==false)
            {
            
                if(Validar.listaDeObjetos.containsKey(Item)==true)
                {
                     Inventario Objeto = Validar.listaDeObjetos.get(Item);
                    try {
                        if(CampoMaterial.getText().isEmpty()==false)
                        {
                             Objeto.setDescripcion(CampoMaterial.getText());
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this, "El nombre del "
                                + "Material es necesario");
                             return;
                        }
                        if(CampoMedidas.getText().isEmpty()==false){
                            Objeto.setMedidas(CampoMedidas.getText());
                        }else{
                            JOptionPane.showMessageDialog(this, "Las medidas del "
                                + "material son necesarias");
                            return;
                        }
                        if(CampoValorUnitario.getText().isEmpty()==false){
                            Objeto.setValorUnitario(Integer.parseInt(CampoValorUnitario.getText()));
                        }else{
                            JOptionPane.showMessageDialog(this, "El valor unitario"
                                + "del material es necesario ");
                            return;
                        }
                        if(CampoCantidad.getText().isEmpty()==false){
                            Objeto.setCantidad(Integer.parseInt(CampoCantidad.getText()));
                        }else{
                            JOptionPane.showMessageDialog(this, "La cantidad "
                                + "del material es necesaria ");
                            return;
                        }
                        if(CampoFecha.getText().isEmpty()==false){
                            Objeto.setFechaIngreso(CampoFecha.getText());
                        }else{
                            JOptionPane.showMessageDialog(this, "La fecha de ingreso"
                                + " del material es necesario ");
                            return;
                        }
                        Validar Admi = new Validar();
                        Admi.EditObjeto(Objeto);
                        CampoMaterial.setText("");
                        CampoMedidas.setText("");
                        CampoValorUnitario.setText("");
                        CampoCantidad.setText("");
                        CampoItem.setText("");
                        CampoFecha.setText("");
                        BotonEliminar.setEnabled(false);
                        BotonEditar.setEnabled(false);
                        JOptionPane.showMessageDialog(this, "Se editaron los datos");
                        }
                        catch (Exception e) 
                        {
                            JOptionPane.showMessageDialog(this, "Hay un error en la lista");
                            return;
                    }
                }
            }
         }
    }//GEN-LAST:event_BotonEditarActionPerformed

